package primitiveData;

public class Feb22DoWhileLoop {

	public static void main(String[] args) {
		int a = 11;
		do {
			System.out.println(a);
			a++;
		} while (a <= 10);
         int b = 12;
         do {
        	 System.out.println(b);
        	 b++;
         }while(b>13);
         
         int x = 239;
         do {
        	 System.out.println(x);
        	 x++;
         }while(x<=240);
         
         int y = 50;
         do {
        	 System.out.println(y);
        	 y++;
         }while(y>51);
	}

}
